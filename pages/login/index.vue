<template>
    <div>
        <!-- Login Form -->
        <form @submit.prevent="submit">
            <input type="text" class="fadeIn second" placeholder="Username" v-model="data.username">
            <input type="text" class="fadeIn third password" placeholder="Password" v-model="data.password">
            <input type="submit" class="fadeIn fourth" value="Log In">
        </form>
    </div>
</template>

<script>
    import swal from 'sweetalert'
    export default {
        layout: 'login',
        auth:false,
        data() {
            return {
                data: {
                    username: null,
                    password: null
                }
            }
        },
        mounted () {
            if(this.$auth.loggedIn) {
                this.$router.push('/')
            }
        },
        methods: {
            async submit() {
                try {
                    let response = await this.$auth.loginWith('local', { data: this.data })
                    this.$router.push('/')
                    swal({
                        text: 'Success Login',
                        icon: 'success',
                        timer: 1200,
                        button: false
                    })
                } catch (err) {
                    console.log(err)
                }
            }
        }
    }
</script>

